package PRACTICA4_ENTRENAMIENTO;
/**
 * Programa Java que declare una variable A de tipo entero y asígnale un
valor. A continuación muestra un mensaje indicando si A es par o impar.
 * @author diurno
 */
public class P04_E13 {

	public static void main(String[] args) {
		byte a = 51;
		System.out.println(a % 2 == 0 ? "A es par." : "A es impar.");
	}

}
